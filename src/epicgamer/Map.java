    public ArrayList<Sprite> makeMap () {
        
        for (int y = 0; y < 18; y++) {
            for (int x = 0; x < 32; x++) {
                blocks.add(new Sprite(x*40, y*40, width, height, findColor(y, x), findType(y, x)));
            }
        }
        return blocks;
        
    }
    
    public int findType (int y, int x) {
        return grid[y][x];
    }
    
    public Color findColor (int y, int x) {
        
        if (grid[y][x] == 0) {
            return Color.WHITESMOKE;
        }
        if (grid[y][x] == 2) {
            return Color.GREENYELLOW;
        }
        else {
            return Color.BLACK;
        }
        
    }
    
    
    
